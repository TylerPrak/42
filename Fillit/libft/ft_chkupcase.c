#include "libft.h"

int		ft_chkupcase(char *str)
{
	int i;

	i = 0;
	while (str[i] >= 97 && str[i] <= 122)
	{
		if (str[i + 1] == '\0')
			return (1);
		i++;
	}
	return (0);
}
